class MisconceptionDetector:

    def __init__(self, groq_client=None):
        self.groq_client = groq_client

    def _rule_level(self, subject, question):
        text = (question or "").lower()
        for rule in _RULES.get(subject, []):
            if any(kw in text for kw in rule["keywords"]):
                return {"type": rule["type"], "description": rule["description"],
                        "hint": rule["hint"], "corrective_steps": list(rule["corrective_steps"]),
                        "level": 1}
        return None

    def _llm_level(self, subject, topic, question, user_answer, correct_answer):
        if self.groq_client is None:
            return self._generic(subject)
        system_prompt = (
            "Ты педагог-диагност. По вопросу, ответу ученика и правильному ответу "
            "определи ТИП концептуальной ошибки. Верни ТОЛЬКО JSON без markdown:\n"
            '{"type":"краткий тип ошибки","description":"в чём заблуждение",'
            '"hint":"как думать правильно","corrective_steps":["шаг1","шаг2","шаг3"]}')
        user_prompt = (
            f"Предмет: {subject}. Тема: {topic}.\n"
            f"Вопрос: {question}\n"
            f"Ответ ученика: {user_answer}\n"
            f"Правильный ответ: {correct_answer}\n"
            "Определи тип ошибки.")
        try:
            r = self.groq_client.chat.completions.create(
                model=GROQ_MODEL,
                messages=[{"role": "system", "content": system_prompt},
                          {"role": "user", "content": user_prompt}],
                temperature=0.4, max_tokens=300)
            raw = re.sub(r"^```(?:json)?\s*|\s*```$", "", r.choices[0].message.content.strip())
            data = json.loads(raw)
            steps = data.get("corrective_steps", [])
            if isinstance(steps, str):
                steps = [steps]
            return {"type": str(data.get("type", "")).strip() or "Концептуальная ошибка",
                    "description": str(data.get("description", "")).strip(),
                    "hint": str(data.get("hint", "")).strip(),
                    "corrective_steps": [str(s).strip() for s in steps if str(s).strip()],
                    "level": 2}
        except Exception as e:
            logger.warning(f"misconception LLM failed: {e}")
            return self._generic(subject)

    def _generic(self, subject):
        t, h, steps = _GENERIC_BY_SUBJECT.get(
            subject, ("Концептуальная ошибка",
                      "Перечитайте условие и проверьте ключевое правило по теме.",
                      ["Перечитайте условие", "Проверьте правило", "Повторите попытку"]))
        return {"type": t, "description": "", "hint": h,
                "corrective_steps": list(steps), "level": 2}

    def detect(self, subject, topic, question, user_answer, correct_answer):
        rule = self._rule_level(subject, question)
        if rule is not None:
            return rule
        return self._llm_level(subject, topic, question, user_answer, correct_answer)
