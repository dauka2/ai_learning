def subjects_for_grade(grade: int) -> list[str]:
    subjects = SUBJECTS_BY_GRADE.get(grade, list(SUBJECTS.keys()))
    return [s for s in subjects if s in SUBJECTS]


def subjects_for_stage(stage: str) -> list[str]:
    grade = _STAGE_TO_GRADE.get(stage)
    if grade is None:
        return list(SUBJECTS.keys())
    return subjects_for_grade(grade)


